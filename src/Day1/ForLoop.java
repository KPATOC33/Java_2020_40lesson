public class ForLoop {
    public static void main(String[] args) {
        //int i - переменная счетчик * Условие i<=10  * i++  условие означает что мы будем делать проходя цикл , так-же i++ == i = i+1
        for (int i = 0 ; i<=10;i++ ){
            System.out.println("Hello"+i);
        }
    }
}
